package br.com.ofm.gnre.util;

public class InfoUtil {
	
	public static void info(String log) {    
        System.out.println("INFO: " + log + "\n");    
    }    
    
    /**  
     * Log Error.  
     * @param log  
     */    
    public static void error(String log) {    
        System.out.println("ERROR: " + log + "\n");    
    }    

}
